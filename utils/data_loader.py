def load_dataloader(args, MyDataset, root_path="./datasets/ETT/", transform=None):
    train_data = MyDataset(root_path=root_path,
                           flag="train", freq=args.freq,
                           size=[args.seq_len, args.label_len, args.pred_len], transform=transform)
    train_loader = DataLoader(train_data,
                              batch_size=args.batch_size,
                              shuffle=True,
                              num_workers=0,
                              drop_last=True)

    eval_data = MyDataset(root_path=root_path,
                          flag="val", freq=args.freq,
                          size=[args.seq_len, args.label_len, args.pred_len], )

    eval_loader = DataLoader(eval_data,
                             batch_size=args.batch_size,
                             shuffle=False,
                             num_workers=0,
                             drop_last=True)

    test_data = MyDataset(root_path=root_path,
                          flag="test", freq=args.freq,
                          size=[args.seq_len, args.label_len, args.pred_len], )

    test_loader = DataLoader(test_data,
                             batch_size=1,
                             shuffle=False,
                             num_workers=0,
                             drop_last=False)
    return train_loader, eval_loader, test_loader

def trans(data):
    alpha = np.random.randn() * 0.05  # 0.8
    beta = np.random.randn() * 0.05  # 0.8
    mean = np.mean(data, 0, keepdims=True)
    std = np.std(data, 0, keepdims=True)
    std_new = std * (alpha + 1)
    mean_new = mean * (beta + 1)

    data = (data - mean) / (std + 1e-7)
    data = data * std_new + mean_new
    return data

def jittering(data):
    alpha = np.random.randn(data.shape[0]) * 0.05  # 0.8
    alpha = alpha.reshape(-1,1)
    data = data + alpha
    return data

def scaling(data):
    alpha = np.random.randn() * 0.05  # 0.8
    data = data * (1+alpha)
    return data

def magnitude(data):
    alpha = np.random.randn(data.shape[0]) * 0.05  # 0.8
    alpha = alpha.reshape(-1,1)
    data = data * (1+alpha)
    return data